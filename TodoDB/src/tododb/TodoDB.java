    private void dlgAddEdit_btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dlgAddEdit_btSaveActionPerformed
        String task = dlgAddEdit_tfTask.getText();
        String dueDateStr = dlgAddEdit_tfDueDate.getText();
        boolean isDone = dlgAddEdit_cbIsDone.isSelected();
        try {
            Todo todo = new Todo(0, task, dueDateStr, isDone);
            if (currentEditedTodo == null) {
                db.addTodo(todo);
            } else {
                todo.setId(currentEditedTodo.getId());
                db.updateTodo(todo);
                currentEditedTodo = null;
            }
            dlgAddEdit.setVisible(false);
            reloadTodos();
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this,
                    "Invalid input: " + e.getMessage(),
                    "Input error",
                    JOptionPane.WARNING_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error adding record: " + e.getMessage(),
                    "Database error",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_dlgAddEdit_btSaveActionPerformed

    // STATE 1: if null - AddEdit Dialog is ADDING
    // STATE 2: if NOT null - AddEdit Dialog is EDITING
    Todo currentEditedTodo = null;

    private void lstTodosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstTodosMouseClicked
        // when double-click happens - edit the item
        if (evt.getClickCount() == 2) {
            int index = lstTodos.locationToIndex(evt.getPoint());
            lstTodos.setSelectedIndex(index);
            currentEditedTodo = modelTodoList.get(index);
            // load data
            dlgAddEdit_tfTask.setText(currentEditedTodo.getTask());
            dlgAddEdit_tfDueDate.setText(currentEditedTodo.getDueDateString());
            dlgAddEdit_cbIsDone.setSelected(currentEditedTodo.isDone());
            dlgAddEdit_btSave.setText("Save changes");
            dlgAddEdit.pack();
            dlgAddEdit.setVisible(true);
        }
    }//GEN-LAST:event_lstTodosMouseClicked

    private void lstTodosMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstTodosMouseReleased
        if (SwingUtilities.isRightMouseButton(evt)) {
            contextMenu.show(lstTodos, evt.getX(), evt.getY());
        }
    }//GEN-LAST:event_lstTodosMouseReleased

    private void miDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miDeleteActionPerformed
        Todo todo = lstTodos.getSelectedValue();
        if (todo == null) {
            return;
        }
        Object[] options = {"Yes, please",
            "No, thanks",
            "No eggs, no ham!"};
        int choice = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete\n"
                + todo.toString(),
                "Confirmation",
                JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            try {
                db.deleteTodoById(todo.getId());
                reloadTodos();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this,
                        "Error adding record: " + e.getMessage(),
                        "Database error",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_miDeleteActionPerformed

