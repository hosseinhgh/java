    public void reloadTodos() {
        try {
            ArrayList<Todo> list = db.getAllTodos();
            modelTodoList.clear();
            for (Todo t : list) {
                modelTodoList.addElement(t);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Error fetching data: " + e.getMessage(),
                    "Database error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

